# Інтерполяційний многочлен Лагранжа
n = 4
xp = 1.76
yp = 0
x = [1.5, 1.6, 1.7, 1.8]
y = [0.0339, 0.0236, 0.0162, 0.0109]

for i in range(n):

    p = 1

    for j in range(n):
        if i != j:
            p = p * (xp - x[j]) / (x[i] - x[j])

    yp = yp + p * y[i]

print('Значення інтерполяції в точці %.2f становить %.4f.' % (xp, yp))